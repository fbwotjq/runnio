SELECT
    SYS_CREATION_DATE,
    ORG_ID,
    USER_ID,
    LOGIN_ID,
    EVENT_TYPE,
    POLICY_RULE_ID,
    POLICY_ACTION,
    USER_DEFINED_EVENT_TYPE,
    AMOUNT_ORIGINAL,
    AMOUNT_CURRENCY,
    PRELIMINARY_SCORE,
    RISK_SCORE,
    FLAGGED,
    IP_ADDRESS,
    EXT_ACCT_COUNTRY,
    EXT_ACCT_ROUTING_CODE,
    EXT_ACCT_NUMBER,
    EXT_ACCT_OWNER_NAME,
    EXT_ACCT_REFERENCE_CODE,
    CDF_S_1,
    CDF_S_2,
    CDF_S_3,
    CDF_S_4,
    CDF_S_5,
    CDF_S_6,
    CDF_S_7
FROM RSA_CORE.EVENT_LOG
WHERE (SYS_CREATION_DATE BETWEEN TO_DATE('2013-01-29', 'YYYY-MM-DD') AND TO_DATE('2013-01-29 23:59:59', 'YYYY-MM-DD HH24:MI:SS')) AND ORG_ID='RBS_12361'
ORDER BY SYS_CREATION_DATE DESC